-- TB_PITCHER_STAT: 투수 시즌 성적
CREATE TABLE TB_PITCHER_STAT (
    ID         NUMBER        GENERATED BY DEFAULT AS IDENTITY,
    PLAYER_ID  VARCHAR2(20)  NOT NULL,
    SEASON     NUMBER(4)     NOT NULL,
    ERA        NUMBER(5,2),
    GAMES      NUMBER(4)     DEFAULT 0 NOT NULL,
    WINS       NUMBER(4)     DEFAULT 0 NOT NULL,
    LOSSES     NUMBER(4)     DEFAULT 0 NOT NULL,
    SAVES      NUMBER(4)     DEFAULT 0 NOT NULL,
    HOLDS      NUMBER(4)     DEFAULT 0 NOT NULL,
    IP         NUMBER(6,1),
    HITS       NUMBER(5)     DEFAULT 0 NOT NULL,
    SO         NUMBER(5)     DEFAULT 0 NOT NULL,
    BB         NUMBER(5)     DEFAULT 0 NOT NULL,
    WHIP       NUMBER(4,2),
    CONSTRAINT PK_TB_PITCHER_STAT PRIMARY KEY (ID)
);

-- 선수-시즌당 1행을 보장하는 UNIQUE 인덱스 (findByPlayerIdAndSeason 단건 조회)
CREATE UNIQUE INDEX UX_PITCHER_STAT_PLAYER_SEASON ON TB_PITCHER_STAT (PLAYER_ID, SEASON);
