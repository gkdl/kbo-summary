-- TB_GAME_SCORE: 이닝별 스코어보드
CREATE TABLE TB_GAME_SCORE (
    ID          NUMBER        GENERATED BY DEFAULT AS IDENTITY,
    GAME_ID     VARCHAR2(20)  NOT NULL,
    INNING      NUMBER(3)     NOT NULL,
    HOME_SCORE  NUMBER(3)     DEFAULT 0 NOT NULL,
    AWAY_SCORE  NUMBER(3)     DEFAULT 0 NOT NULL,
    HOME_R      NUMBER(3)     DEFAULT 0 NOT NULL,
    HOME_H      NUMBER(3)     DEFAULT 0 NOT NULL,
    HOME_E      NUMBER(3)     DEFAULT 0 NOT NULL,
    HOME_B      NUMBER(3)     DEFAULT 0 NOT NULL,
    AWAY_R      NUMBER(3)     DEFAULT 0 NOT NULL,
    AWAY_H      NUMBER(3)     DEFAULT 0 NOT NULL,
    AWAY_E      NUMBER(3)     DEFAULT 0 NOT NULL,
    AWAY_B      NUMBER(3)     DEFAULT 0 NOT NULL,
    CONSTRAINT PK_TB_GAME_SCORE PRIMARY KEY (ID)
);

CREATE INDEX IX_GAME_SCORE_GAME ON TB_GAME_SCORE (GAME_ID);
