-- TB_STANDING: 시즌 팀 순위
-- RANK는 Oracle 분석 함수명과 겹치므로 컬럼명을 TEAM_RANK로 사용
CREATE TABLE TB_STANDING (
    ID            NUMBER        GENERATED BY DEFAULT AS IDENTITY,
    SEASON        NUMBER(4)     NOT NULL,
    TEAM_CODE     VARCHAR2(10)  NOT NULL,
    TEAM_RANK     NUMBER(3)     NOT NULL,
    WINS          NUMBER(4)     DEFAULT 0 NOT NULL,
    LOSSES        NUMBER(4)     DEFAULT 0 NOT NULL,
    DRAWS         NUMBER(4)     DEFAULT 0 NOT NULL,
    WIN_RATE      NUMBER(4,3),
    GAMES_BEHIND  NUMBER(4,1),
    CONSTRAINT PK_TB_STANDING PRIMARY KEY (ID)
);

CREATE INDEX IX_STANDING_SEASON_RANK ON TB_STANDING (SEASON, TEAM_RANK);
