-- TB_HITTER_STAT: 타자 시즌 성적
CREATE TABLE TB_HITTER_STAT (
    ID         NUMBER        GENERATED BY DEFAULT AS IDENTITY,
    PLAYER_ID  VARCHAR2(20)  NOT NULL,
    SEASON     NUMBER(4)     NOT NULL,
    AVG        NUMBER(4,3),
    GAMES      NUMBER(4)     DEFAULT 0 NOT NULL,
    AB         NUMBER(5)     DEFAULT 0 NOT NULL,
    HITS       NUMBER(5)     DEFAULT 0 NOT NULL,
    DOUBLES    NUMBER(4)     DEFAULT 0 NOT NULL,
    TRIPLES    NUMBER(4)     DEFAULT 0 NOT NULL,
    HR         NUMBER(4)     DEFAULT 0 NOT NULL,
    RBI        NUMBER(5)     DEFAULT 0 NOT NULL,
    RUNS       NUMBER(5)     DEFAULT 0 NOT NULL,
    SB         NUMBER(4)     DEFAULT 0 NOT NULL,
    BB         NUMBER(5)     DEFAULT 0 NOT NULL,
    SO         NUMBER(5)     DEFAULT 0 NOT NULL,
    OPS        NUMBER(5,3),
    CONSTRAINT PK_TB_HITTER_STAT PRIMARY KEY (ID)
);

-- 선수-시즌당 1행을 보장하는 UNIQUE 인덱스 (findByPlayerIdAndSeason 단건 조회)
CREATE UNIQUE INDEX UX_HITTER_STAT_PLAYER_SEASON ON TB_HITTER_STAT (PLAYER_ID, SEASON);
