-- TB_GAME_BOX_PITCHER: 경기별 투수 박스스코어
CREATE TABLE TB_GAME_BOX_PITCHER (
    ID              NUMBER         GENERATED BY DEFAULT AS IDENTITY,
    GAME_ID         VARCHAR2(20)   NOT NULL,
    TEAM_CODE       VARCHAR2(10)   NOT NULL,
    PLAYER_NAME     VARCHAR2(50)   NOT NULL,
    ROLE            VARCHAR2(20),
    DECISION        VARCHAR2(10),
    WINS            NUMBER(3)      DEFAULT 0 NOT NULL,
    LOSSES          NUMBER(3)      DEFAULT 0 NOT NULL,
    SAVES           NUMBER(3)      DEFAULT 0 NOT NULL,
    INNINGS_PITCHED NUMBER(5,1),
    BATTERS_FACED   NUMBER(3)      DEFAULT 0 NOT NULL,
    PITCH_COUNT     NUMBER(4)      DEFAULT 0 NOT NULL,
    AT_BATS         NUMBER(3)      DEFAULT 0 NOT NULL,
    HITS            NUMBER(3)      DEFAULT 0 NOT NULL,
    HOME_RUNS       NUMBER(3)      DEFAULT 0 NOT NULL,
    WALKS           NUMBER(3)      DEFAULT 0 NOT NULL,
    STRIKE_OUTS     NUMBER(3)      DEFAULT 0 NOT NULL,
    RUNS            NUMBER(3)      DEFAULT 0 NOT NULL,
    EARNED_RUNS     NUMBER(3)      DEFAULT 0 NOT NULL,
    ERA             NUMBER(5,2),
    CONSTRAINT PK_TB_GAME_BOX_PITCHER PRIMARY KEY (ID)
);

CREATE INDEX IX_BOX_PITCHER_GAME ON TB_GAME_BOX_PITCHER (GAME_ID);
