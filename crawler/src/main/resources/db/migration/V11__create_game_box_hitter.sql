-- TB_GAME_BOX_HITTER: 경기별 타자 박스스코어
CREATE TABLE TB_GAME_BOX_HITTER (
    ID              NUMBER         GENERATED BY DEFAULT AS IDENTITY,
    GAME_ID         VARCHAR2(20)   NOT NULL,
    TEAM_CODE       VARCHAR2(10)   NOT NULL,
    PLAYER_NAME     VARCHAR2(50)   NOT NULL,
    BATTING_ORDER   NUMBER(2),
    POSITION        VARCHAR2(10),
    AT_BATS         NUMBER(3)      DEFAULT 0 NOT NULL,
    HITS            NUMBER(3)      DEFAULT 0 NOT NULL,
    RBI             NUMBER(3)      DEFAULT 0 NOT NULL,
    RUNS            NUMBER(3)      DEFAULT 0 NOT NULL,
    AVG             NUMBER(5,3),
    CONSTRAINT PK_TB_GAME_BOX_HITTER PRIMARY KEY (ID)
);

CREATE INDEX IX_BOX_HITTER_GAME ON TB_GAME_BOX_HITTER (GAME_ID);
